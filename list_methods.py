#List methods

kuchbhi = ["Rahul", "Arijit",889,"Oranges", False,0,6.99]
print(kuchbhi)

#append() adds an element at the last of the last
kuchbhi.append("Deepu")
print(kuchbhi)

#sort() sorts a number at ascending order
n1 = [133,45,600,6,2,1,160,546,999,0,11]
n1.sort()
print(n1)

#insert() adds an element at a specific index
n1.insert(2,2024)
# 2 is the index value and 2024 is the element to be added
print(n1)

#reverse() reverse a orginal list 
n2=[89,0,100,1,9,2000,12345]
n2.reverse()
print(n2)

#pop() removes or delete a element from the given index
n2.pop(2)
print(n2)

#remove() deletes an element from the list
n3=["Rajesh","Deepu","Rahul",8900,98.999,False]
print(n3)
n3.remove("Rajesh")
print(n3)


